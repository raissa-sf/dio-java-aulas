/**
 * A classe Calculadora realiza operações matemáticas simples.
 * 
 * @autor Raissa Feitosa
 * @version 1.0
 * @since 04/03/2024
 */
public class Calculadora {

    /**
     * Soma dois números inteiros.
     *
     * @param numeroUm   O primeiro número a ser somado.
     * @param numeroDois O segundo número a ser somado.
     * @return O resultado da soma de numeroUm e numeroDois.
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
